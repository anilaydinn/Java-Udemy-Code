
public class Resolution {

	private int en;
	private int boy;
	
	public Resolution(int en, int boy) {
		
		this.en = en;
		this.boy = boy;
	}
	
	public void setEn(int en) {
		
		this.en = en;
	}
	
	public int getEn() {
		
		return this.en;
	}
	
	public void setBoy(int boy) {
		
		this.boy = boy;
	}
	
	public int getBoy() {
		
		return this.boy;
	}
}
